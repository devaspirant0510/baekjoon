import java.io.*;
import java.util.*;

public class Main {
    public static void main(String[] args) throws IOException {
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        BufferedWriter bw = new BufferedWriter(new OutputStreamWriter(System.out));
        String text = br.readLine();
        int n = Integer.parseInt(br.readLine());
        int curPos = text.length();
        List<Character> editor = new LinkedList<>();
        for (int i = 0; i < text.length(); i++) {
            editor.add(text.charAt(i));
        }
        ListIterator<Character> cursorStat = editor.listIterator();
        while(cursorStat.hasNext()){
            cursorStat.next();
        }
        StringTokenizer st;
        for (int i = 0; i < n; i++) {
            st = new StringTokenizer(br.readLine());
            String prefix = st.nextToken();
            switch (prefix) {
                case "L":
                    if (cursorStat.hasPrevious()) {
                        cursorStat.previous();
                    }
                    break;
                case "D":
                    if (cursorStat.hasNext()) {
                        cursorStat.next();
                    }
                    break;
                case "B":
                    if(cursorStat.hasPrevious()){
                        cursorStat.previous();
                        cursorStat.remove();

                    }
                    break;
                case "P":
                    String addText = st.nextToken();
                    cursorStat.add(addText.charAt(0));
                    break;
            }
        }
        Iterator<Character> iterator = editor.iterator();
        StringBuilder sb = new StringBuilder();
        while (iterator.hasNext()) {
            sb.append(iterator.next()+"");
        }
        bw.write(String.valueOf(sb));
        bw.flush();
        bw.close();
    }
}

