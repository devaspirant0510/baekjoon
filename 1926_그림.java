import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;
import java.util.StringTokenizer;

public class Main {
    private static int[] dx = {-1, 1, 0, 0};
    private static int[] dy = {0, 0, -1, 1};
    private static int n,m;

    public static void main(String[] args) throws IOException {
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        String[] line = br.readLine().split(" ");
        n = Integer.parseInt(line[0]);
        m = Integer.parseInt(line[1]);
        int[][] arr = new int[n][m];
        boolean[][] cache = new boolean[n][m];
        StringTokenizer st;
        for (int i = 0; i < n; i++) {
            st = new StringTokenizer(br.readLine());
            for (int j = 0; j < m; j++) {
                int el = Integer.parseInt(st.nextToken());
                arr[i][j] = el;
            }
        }
        int maxValue =0;
        int c =0;
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < m; j++) {
                if(arr[i][j]==1 && !cache[i][j]){
                    int r = dfs(arr,cache,i,j);
                    if(maxValue<r){
                        maxValue = r;
                    }
                    c++;
                }
                
            }
        }
        System.out.println(c);
        System.out.println(maxValue);
    }

    public static int dfs(int[][] arr, boolean[][] cache, int y, int x) {
        cache[y][x] = true;
        int area = 1; // ìê¸° ìì‹  í•œ ì¹¸ í¬í•¨

        for (int i = 0; i < 4; i++) {
            int goY = dy[i] + y;
            int goX = dx[i] + x;

            if (isGo(goY, goX) && !cache[goY][goX] && arr[goY][goX] == 1) {
                area += dfs(arr, cache, goY, goX);
            }
        }

        return area;
    }
    public static boolean isGo(int goY,int goX){
        if(0<=goX && goX<m && 0<=goY && goY<n){
            return true;
        }
        return false;
    }
}

