import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;
import java.util.HashSet;
import java.util.Set;

public class Main {
    private static int r, c;
    private static int[] dx = {-1, 1, 0, 0};
    private static int[] dy = {0, 0, -1, 1};
    private static boolean[] cache = new boolean[26];
    private static int maxValue=-181818;


    public static void main(String[] args) throws IOException {
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        String[] line = br.readLine().split(" ");
        r = Integer.parseInt(line[0]);
        c = Integer.parseInt(line[1]);
        int[][] arr = new int[r][c];

        for (int i = 0; i < r; i++) {
            String st = br.readLine();
            for (int j = 0; j < c; j++) {
                arr[i][j] = st.charAt(j);
            }
        }

        cache[arr[0][0]-'A'] = true;
        dfs(0, 0, arr, 1);
        System.out.println(maxValue);
    }

    public static void dfs(int y, int x, int[][] arr, int dpeth) {
        maxValue = Math.max(dpeth,maxValue);
        for (int i = 0; i < 4; i++) {
            int goY = y + dy[i];
            int goX = x + dx[i];
            if (isGo(goY, goX)) {
                if(!cache[arr[goY][goX]-'A']){
                    cache[arr[goY][goX]-'A'] = true;
                    dfs(goY, goX, arr, dpeth+1);
                    cache[arr[goY][goX]-'A'] = false;
                }
            }
        }
    }

    protected static boolean isGo(int goY, int goX) {
        if (0 <= goX && goX < c && 0 <= goY && goY < r) {
            return true;
        }
        return false;
    }
}

