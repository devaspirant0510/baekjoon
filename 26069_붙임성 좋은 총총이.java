import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;
import java.util.ArrayList;
import java.util.List;

public class Main {
    private static final String RAINBOW_MAN = "ChongChong";
    public static void main(String[] args) throws IOException {
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        int n = Integer.parseInt(br.readLine());
        List<String> rainbowMan = new ArrayList<>();
        rainbowMan.add(RAINBOW_MAN);
        for (int i = 0; i < n; i++) {
            String[] peoples = br.readLine().split(" ");
            // 왼쪽 사람이 레인보우일경우
            if(rainbowMan.contains(peoples[0])){
                // 이미 레인보우인사람은 안넣어도 됨
                if(!rainbowMan.contains(peoples[1])){
                    rainbowMan.add(peoples[1]);
                }

            }
            else if(rainbowMan.contains(peoples[1])){
                // 이미 레인보우인사람은 안넣어도 됨
                if(!rainbowMan.contains(peoples[0])){
                    rainbowMan.add(peoples[0]);
                }

            }
        }
        System.out.println(rainbowMan.size());
    }
}

