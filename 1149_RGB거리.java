import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;

public class Main {
    private static final int R = 0;
    private static final int G = 1;
    private static final int B = 2;
    private static int[][] selectedRGB;
    private static int n;

    public static void main(String[] args) throws IOException {
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        int[][] dp = new int[1001][3];
        int[][] arr = new int[1001][3];
        selectedRGB = new int[1001][3];
        n = Integer.parseInt(br.readLine());
        for (int i = 1; i <= n; i++) {
            String[] rgb = br.readLine().split(" ");
            if (i == 1) {
                dp[i][R] = Integer.parseInt(rgb[R]);
                selectedRGB[i][R] = R;
                dp[i][G] = Integer.parseInt(rgb[G]);
                selectedRGB[i][G] = G;
                dp[i][B] = Integer.parseInt(rgb[B]);

                selectedRGB[i][B] = B;
            }
            arr[i][R] = Integer.parseInt(rgb[R]);
            arr[i][G] = Integer.parseInt(rgb[G]);
            arr[i][B] = Integer.parseInt(rgb[B]);
        }
        for (int i = 1; i <= n; i++) {
            dp[i][R] = Math.min(dp[i - 1][G], dp[i - 1][B]) + arr[i][R];
            dp[i][G] = Math.min(dp[i - 1][R], dp[i - 1][B]) + arr[i][G];
            dp[i][B] = Math.min(dp[i - 1][R], dp[i - 1][G]) + arr[i][B];
        }
        System.out.println(Math.min(Math.min(dp[n][R], dp[n][B]), dp[n][G]));
    }

}
