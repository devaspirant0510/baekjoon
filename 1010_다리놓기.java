import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;

public class Main {
    public static void main(String[] args) throws IOException {
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        int N = Integer.parseInt(br.readLine());
        long[] fact = new long[31];
        fact[0] = 1;
        fact[1] = 1L;
        fact[2] = 2L;
        fact[3] = 6L;
        for (int i = 4; i < 31; i++) {

            fact[i] = i * fact[i - 1];
        }
        for (int i = 0; i < N; i++) {
            String[] line = br.readLine().split(" ");
            int n = Integer.parseInt(line[1]);
            int r = Integer.parseInt(line[0]);
            // nCr = n!/(n-r)!*r!
            // ê·¸ëƒ¥ êµ¬í•´ë²„ë¦¬ë©´ ìˆ«ìê°€ ë„ˆë¬´ ì»¤ì§€ê¸°ë–„ë¬¸ì— ë‚˜ëˆ ì„œ ê³„ì‚°, ë¶„ì n! ê³¼ ë¶„ëª¨ (n-r)! ì´ë‚˜ r! ì¤‘ì—ì„œ ë¶„ëª¨ê°’ì´ ë” í°ê°’ì„ ë¶„ì n! ì´ë‘ ë‚˜ëˆ”
            long rFac;
            long nrFac;
            // ë¶„ëª¨ì¤‘ì—ì„œ r! ê°’ì´ ë” í´ê²½ìš°
            // n!/r! == r+1* r+2 .... * n!
            // ì´ë ‡ê²Œ ì—°ì‚°íšŸìˆ˜ë¥¼ ì¤„ì´ê³  (n-r)! ì€ ê·¸ëƒ¥ êµ¬í•¨
            if (n - r < r) {
                rFac = fact[n - r];
                nrFac = factorial(r, n);
            } else {
                rFac = fact[r];
                nrFac = factorial(n - r, n);
            }
            System.out.println(nrFac / rFac);

        }


    }

    private static long factorial(long s, long e) {
        long sum = 1;
        for (long i = s + 1; i <= e; i++) {
            sum *= i;
        }
        return sum;
    }
}


