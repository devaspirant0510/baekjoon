import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;

public class Main {
    public static void main(String[] args) throws IOException {
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        int N = Integer.parseInt(br.readLine());
        long[] fact = new long[31];
        fact[0] = 1;
        fact[1] = 1L;
        fact[2] = 2L;
        fact[3] = 6L;
        for (int i = 4; i < 31; i++) {

            fact[i] = i * fact[i - 1];
        }
        for (int i = 0; i < N; i++) {
            String[] line = br.readLine().split(" ");
            int n = Integer.parseInt(line[1]);
            int r = Integer.parseInt(line[0]);
            // nCr = n!/(n-r)!*r!
            // 그냥 구해버리면 숫자가 너무 커지기떄문에 나눠서 계산, 분자 n! 과 분모 (n-r)! 이나 r! 중에서 분모값이 더 큰값을 분자 n! 이랑 나눔
            long rFac;
            long nrFac;
            // 분모중에서 r! 값이 더 클경우
            // n!/r! == r+1* r+2 .... * n!
            // 이렇게 연산횟수를 줄이고 (n-r)! 은 그냥 구함
            if (n - r < r) {
                rFac = fact[n - r];
                nrFac = factorial(r, n);
            } else {
                rFac = fact[r];
                nrFac = factorial(n - r, n);
            }
            System.out.println(nrFac / rFac);

        }


    }

    private static long factorial(long s, long e) {
        long sum = 1;
        for (long i = s + 1; i <= e; i++) {
            sum *= i;
        }
        return sum;
    }
}


