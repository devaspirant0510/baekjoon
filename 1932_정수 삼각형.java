import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;
import java.util.StringTokenizer;

public class Main {
    public static void main(String[] args) throws IOException {
        // 1. n*n dp[y][x] 배열
        // 2. 각 행의 시작과 끝은 항상 부모값 고정적으로
        //     - 0열은 dp[y-1][0]
        //     - n열은 dp[y-1][n-1]
        // 3. 나머지는 둘중에 큰게 dp 에 저장.
        //    dp[y-1][x-1] vs dp[y-1][x]
        //
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        int n = Integer.parseInt(br.readLine());

        // 입력용
        int[][] arr = new int[n][n];
        // 디피용
        int[][] dp = new int[n][n];

        StringTokenizer st ;

        for (int i = 0; i < n; i++) {
            st = new StringTokenizer(br.readLine());
            for (int j = 0; j <i+1 ; j++) {
                arr[i][j] = Integer.parseInt(st.nextToken());

            }
            
        }
        dp[0][0] = arr[0][0];
        for (int i = 1; i <n ; i++) {
            for (int j = 0; j < i+1; j++) {
                // 시작하는 열
                int currentValue = arr[i][j];
                if(j==0){
                    int prevValue = dp[i - 1][j];
                    dp[i][j] = currentValue + prevValue;
                }
                // 끝나는 열
                else if(j==i){
                    int prevValue = dp[i - 1][j - 1];
                    dp[i][j] = currentValue + prevValue;
                }
                // 나머지 열은 둘중에 큰값
                else{
                    int lvalue = dp[i - 1][j - 1];
                    int rvalue = dp[i - 1][j];
                    dp[i][j] = currentValue + Math.max(lvalue, rvalue);
                }
            }
        }
        int max = -1;
        for (int i = 0; i < n; i++) {
            if(dp[n-1][i]>max){
                max = dp[n-1][i];
            }

        }
        System.out.println(max);


    }
}
