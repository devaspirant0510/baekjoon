import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;
import java.util.LinkedList;
import java.util.Queue;

public class Main {
    public static void main(String[] args) throws IOException {
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        int n = Integer.parseInt(br.readLine());
        String[] line = br.readLine().split(" ");
        int startPos = Integer.parseInt(line[0]);
        int endPos = Integer.parseInt(line[1]);
        int m = Integer.parseInt(br.readLine());
        boolean[][] arr = new boolean[n][n];
        boolean[] visited = new boolean[n];
        // 최단 경로를 구해야 제대로 촌수를 구할수 있따
        for (int i = 0; i < m; i++) {
            String[] rel = br.readLine().split(" ");
            int l = Integer.parseInt(rel[0]);
            int r = Integer.parseInt(rel[1]);
            arr[l-1][r-1] = true;
            arr[r-1][l-1] = true;
        }
        Queue<int[]> q = new LinkedList<>();
        for (int i = 0; i < n; i++) {
            // 시작 지점이랑 바로 연결된 애들은 1촌임
            if(arr[startPos-1][i]){
                q.add(new int[]{i,1});
            }
        }
        // 방문처리
        visited[startPos-1] = true;
        while(!q.isEmpty()){
            int[] pos = q.remove();
            int curNode = pos[0];
            int chon = pos[1];
            if(curNode == endPos-1){
                System.out.println(chon);
                return;
            }
            for (int i = 0; i <n ; i++) {
                // 만약 큐에서 뺀 노드랑 연결된 애들은 촌수 증가
                if(arr[curNode][i] &&!visited[i]){
                    visited[i]= true;
                    q.add(new int[]{i,chon+1});
                }
            }
        }
        System.out.println(-1);
    }
}

