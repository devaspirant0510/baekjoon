import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;
import java.util.LinkedList;
import java.util.Queue;

public class Main {
    public static void main(String[] args) throws IOException {
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        int n = Integer.parseInt(br.readLine());
        String[] line = br.readLine().split(" ");
        int startPos = Integer.parseInt(line[0]);
        int endPos = Integer.parseInt(line[1]);
        int m = Integer.parseInt(br.readLine());
        boolean[][] arr = new boolean[n][n];
        boolean[] visited = new boolean[n];
        // ìµœë‹¨ ê²½ë¡œë¥¼ êµ¬í•´ì•¼ ì œëŒ€ë¡œ ì´Œìˆ˜ë¥¼ êµ¬í• ìˆ˜ ìˆë”°
        for (int i = 0; i < m; i++) {
            String[] rel = br.readLine().split(" ");
            int l = Integer.parseInt(rel[0]);
            int r = Integer.parseInt(rel[1]);
            arr[l-1][r-1] = true;
            arr[r-1][l-1] = true;
        }
        Queue<int[]> q = new LinkedList<>();
        for (int i = 0; i < n; i++) {
            // ì‹œì‘ ì§€ì ì´ë‘ ë°”ë¡œ ì—°ê²°ëœ ì• ë“¤ì€ 1ì´Œì„
            if(arr[startPos-1][i]){
                q.add(new int[]{i,1});
            }
        }
        // ë°©ë¬¸ì²˜ë¦¬
        visited[startPos-1] = true;
        while(!q.isEmpty()){
            int[] pos = q.remove();
            int curNode = pos[0];
            int chon = pos[1];
            if(curNode == endPos-1){
                System.out.println(chon);
                return;
            }
            for (int i = 0; i <n ; i++) {
                // ë§Œì•½ íì—ì„œ ëº€ ë…¸ë“œë‘ ì—°ê²°ëœ ì• ë“¤ì€ ì´Œìˆ˜ ì¦ê°€
                if(arr[curNode][i] &&!visited[i]){
                    visited[i]= true;
                    q.add(new int[]{i,chon+1});
                }
            }
        }
        System.out.println(-1);
    }
}

